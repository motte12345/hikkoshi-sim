/**
 * Look up the road distance in km between two prefecture capitals by prefecture name.
 * Returns 0 if either prefecture name is not found.
 *
 * @example
 *   getPrefectureDistance('東京都', '大阪府') // => 515
 *   getPrefectureDistance('北海道', '沖縄県') // => 2920
 */
export function getPrefectureDistance(from: Prefecture, to: Prefecture): number {
  const fromIdx = PREFECTURES.indexOf(from);
  const toIdx = PREFECTURES.indexOf(to);
  if (fromIdx === -1 || toIdx === -1) return 0;
  return distanceMatrix[fromIdx][toIdx];
}

/**
 * Convenience: get the full distance map for one prefecture to all others.
 * Returns a Record keyed by prefecture name.
 */
export function getPrefectureDistances(from: Prefecture): Record<Prefecture, number> {
  const fromIdx = PREFECTURES.indexOf(from);
  const result = {} as Record<Prefecture, number>;
  for (let i = 0; i < PREFECTURES.length; i++) {
    result[PREFECTURES[i]] = fromIdx === -1 ? 0 : distanceMatrix[fromIdx][i];
  }
  return result;
}

